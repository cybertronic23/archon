        let bytes = content.len();
        fs::write(file_path, content).await.map_err(|e| {
            anyhow::anyhow!("Failed to write file '{file_path}': {e}")
        })?;

        Ok(format!("Successfully wrote {bytes} bytes to '{file_path}'."))
